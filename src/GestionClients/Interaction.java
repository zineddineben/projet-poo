package GestionClients;

import java.util.Date;

public class Interaction {
    
    private int id;
    private Date date;
    private String type;
    private String details;

    public int getId() {
        return id;
    }

    public Date getDate() {
        return date;
    }

    public String getType() {
        return type;
    }

    public String getDetails() {
        return details;
    }
}

